      DO I = 1, N_MESH_M                                            !
        READ(IUI3,20,END=200) R(I),COREWF(I)                        !
      END DO                                                        !
!
 200  CLOSE(IUI3)                                                   !
!
!  Formats:
!
  10  FORMAT(A34)
  20  FORMAT(2E13.6)
!
 110  FORMAT(///,10X,'<<<<<<<<<<   WRONG CORE STATE FILE:    ',     &
                '>>>>>>>>>>',/,10X,'<<<<<<<<<<  SHOULD BE ON ',     &
                'LIN-LOG MESH  >>>>>>>>>>')
!
      END SUBROUTINE READ_CORE_WF
