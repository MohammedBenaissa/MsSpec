MODULE BINOMIAL
!
!  This module defines the values of the binomial coefficients
!
      USE ACCURACY_REAL
!
CONTAINS
!
!=======================================================================
!
      FUNCTION C(N,K)
!                                                    k               n!
!  This function computes the binomial coefficients C  = ( n ) = -----------
!                                                    n   ( k )    k! (n-k)!
!
!  Input parameters:
!
!       * N,K      : input integer numbers
!
!  Output variables :
!
!       * C        : binomial coefficient C(n,k)
!
!
!   Author :  D. SÃ©billeau
!
!                                           Last modified :  7 Aug 2020
!
!
      IMPLICIT NONE
!
      REAL (WP)             ::  C
      REAL (WP)             ::  S(0:10,0:10)
!
      INTEGER               ::  N,K
      INTEGER               ::  I,J
!
!  Initialization
!
      DO I=0,10                                                     !
        DO J=0,10                                                   !
          S(I,J)=0.0E0_WP                                           !
        END DO                                                      !
      END DO                                                        !
!
!  Particular values
!
      DO I=0,10                                                     !
        S(I,0)=1.0E0_WP                                             !
        S(I,1)=DFLOAT(I)                                            !
        IF(I > 0) THEN                                              ! ADDED
          S(I,I-1)=DFLOAT(I)                                        !
        END IF                                                      ! ADDED
        S(I,I)=1.0E0_WP                                             !
      END DO
!
      S(4,2)=6.0E0_WP                                               !
!
      S(5,2)=10.0E0_WP                                              !
      S(5,3)=10.0E0_WP                                              !
!
      S(6,2)=15.0E0_WP                                              !
      S(6,3)=20.0E0_WP                                              !
      S(6,4)=15.0E0_WP                                              !
!
      S(7,2)=21.0E0_WP                                              !
      S(7,3)=35.0E0_WP                                              !
      S(7,4)=35.0E0_WP                                              !
      S(7,5)=21.0E0_WP                                              !
!
      S(8,2)=28.0E0_WP                                              !
      S(8,3)=56.0E0_WP                                              !
      S(8,4)=70.0E0_WP                                              !
      S(8,5)=56.0E0_WP                                              !
      S(8,6)=28.0E0_WP                                              !
!
      S(9,2)= 36.0E0_WP                                             !
      S(9,3)= 84.0E0_WP                                             !
      S(9,4)=126.0E0_WP                                             !
      S(9,5)=126.0E0_WP                                             !
      S(9,6)= 84.0E0_WP                                             !
      S(9,7)= 36.0E0_WP                                             !
!
      S(10,2)= 45.0E0_WP                                            !
      S(10,3)=120.0E0_WP                                            !
      S(10,4)=210.0E0_WP                                            !
      S(10,5)=252.0E0_WP                                            !
      S(10,6)=210.0E0_WP                                            !
      S(10,7)=120.0E0_WP                                            !
      S(10,8)= 45.0E0_WP                                            !
!
      IF(K <= N) THEN                                               !
        C=S(N,K)                                                    !
      ELSE                                                          !
        C=0.0E0_WP                                                  !
      END IF                                                        !
!
      END FUNCTION C  
!
END MODULE BINOMIAL
